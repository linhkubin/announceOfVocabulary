    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:

            if (numberOfQuest == numberOfQuests) {
                super.dispose();
                ViewMain vm = new ViewMain();
                vm.setVisible(true);
                vm.setLocationRelativeTo(null);
            }
            if(rdLocationAns == _checkChoice()) result++;
            numberOfQuest++;
            if(numberOfQuest < numberOfQuests){
                _removeChoice();
                _initQuests();
            }
            else{
                _initResult();
            }
        
    }//GEN-LAST:event_nextActionPerformed

    private void choiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choiceActionPerformed
        // TODO add your handling code here:
        
        if (_checkChoice() != 0) {
            if (numberOfQuest == numberOfQuests) {
                super.dispose();
                ViewMain vm = new ViewMain();
                vm.setVisible(true);
                vm.setLocationRelativeTo(null);
            }
            if(rdLocationAns == _checkChoice()) result++;
            numberOfQuest++;
            if(numberOfQuest < numberOfQuests){
                _initQuests();
            }
            else{
                _initResult();
            }
            _removeChoice();
        }
    }//GEN-LAST:event_choiceActionPerformed

    
    void _removeChoice(){
        ans1.setSelected(false);
        ans2.setSelected(false);
        ans3.setSelected(false);
        ans4.setSelected(false);
    }
    int _checkChoice(){
        if (ans1.isSelected()) {
            return 1;
        } 
        if (ans2.isSelected()) {
            return 2;
        }
        if (ans3.isSelected()) {
            return 3;
        }
        if (ans4.isSelected()) {
            return 4;
        }
        return 0;
    }
