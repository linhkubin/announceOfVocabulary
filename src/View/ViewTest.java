    int _rdom(){
        int rdom;
        do{
                rdom = rd.nextInt(list.size());
        }while(rdom == rdQuests);
        return rdom;
    }
    
    void _randomQuests(){
        rdSubjects = rd.nextInt(StopPoint)+1; //random tu - den diem dung
        list = listSubject.get(rdSubjects).listQuests;
        rdQuests = rd.nextInt(list.size());//random tu 0 den so phan tu trong subject dc random// cau hoi that
        rdLocationAns = rd.nextInt(4)+1;
    }
    
    void _initQuests(){
        _randomQuests();
        
        //set random answer
        {            
            ans1.setText(list.get(_rdom()).getAnswer());
            ans2.setText(list.get(_rdom()).getAnswer());
            ans3.setText(list.get(_rdom()).getAnswer());
            ans4.setText(list.get(_rdom()).getAnswer());
        }
        //set real answer
        switch (rdLocationAns) {
            case 1:
                ans1.setText(list.get(rdQuests).getAnswer());
                break;
            case 2:
                ans2.setText(list.get(rdQuests).getAnswer());
                break;
            case 3:
                ans3.setText(list.get(rdQuests).getAnswer());
                break;
            case 4:
                ans4.setText(list.get(rdQuests).getAnswer());
                break;
            
            default:
                throw new AssertionError();
        }
        quest.setText(list.get(rdQuests).getAsk());
        title.setText(numberOfQuest+".."+listSubject.get(rdSubjects).getName());
        
        
        
        
    }

